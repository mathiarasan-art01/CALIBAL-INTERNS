public class numbersandstrings {
    public static void main(String[] args) {
        // Demonstrating numbers
        int num1 = 100;
        int num2 = 250;
        int sum = num1 + num2;
        System.out.println("Sum of numbers: " + sum);

        // Demonstrating strings
        String greeting = "Hello, ";
        String name = "World!";
        String content = greeting + name;
        System.out.println("Concatenated String: " + content);
    }
}
